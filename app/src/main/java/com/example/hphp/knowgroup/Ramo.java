package com.example.hphp.knowgroup;

public class Ramo {
    private String nombre;
    private String lugar;

    public Ramo(){}

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getLugar() {
        return lugar;
    }

    public void setLugar(String lugar) {
        this.lugar = lugar;
    }
}
